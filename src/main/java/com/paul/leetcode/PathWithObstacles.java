    public List<List<Integer>> pathWithObstacles(int[][] obstacleGrid) {
        if (Objects.isNull(obstacleGrid) || obstacleGrid.length == 0 || obstacleGrid[0].length == 0) {
            return new ArrayList<>();
        }

        int row = obstacleGrid.length;
        int column = obstacleGrid[0].length;

        if (obstacleGrid[0][0] == 1 || obstacleGrid[row - 1][column - 1] == 1) {
            return new ArrayList<>();
        }

        if (row == 1 && column == 1) {
            return Arrays.asList(Arrays.asList(0, 0));
        }

        if ((column == 1 || obstacleGrid[row - 1][column - 2] == 1) && (row == 1 || obstacleGrid[row - 2][column - 1] == 1)){
            return new ArrayList<>();
        }

        LinkedList<List<Integer>> path = new LinkedList<>();
        
        if (certainLocationPath(obstacleGrid, 0, 0, path)) {
            path.addFirst(Arrays.asList(0, 0));
        }
        
        return path;

    }

    private boolean certainLocationPath(int[][] obstacleGrid, int rStart, int cStart, LinkedList<List<Integer>> path) {
        obstacleGrid[rStart][cStart] = 1;

        int row = obstacleGrid.length;
        int column = obstacleGrid[0].length;

        if (rStart == row - 1 && cStart == column - 1) {
            return true;
        }

        if (cStart + 1 < column && obstacleGrid[rStart][cStart + 1] == 0 && certainLocationPath(obstacleGrid, rStart, cStart + 1, path)) {
            path.addFirst(Arrays.asList(rStart, cStart + 1));
            return true;
        } else if (rStart + 1 < row && obstacleGrid[rStart + 1][cStart] == 0 && certainLocationPath(obstacleGrid, rStart + 1, cStart, path)) {
            path.addFirst(Arrays.asList(rStart + 1, cStart));
            return true;
        }

        return false;
    }

